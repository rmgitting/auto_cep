    private void initializeRules() {
        File[] files = Global.selectFiles(this, "JSON files (*.json)", "json");
        if (files != null) {
            if (esper != null) {
                CleanUp.killEsperEngine(this);
                engineKilled = true;
            }
            rules = Input.readSequences(files);
            rules.stream().forEach(r -> r.fix());
        }
    }

