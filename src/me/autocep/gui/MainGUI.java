    private void transformBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transformBtnActionPerformed
//        if(rules == null){
//            Global.writeLog(infoPane, Color.RED, "You need to select the rules first");
//            return;
//        }
        if (!engineKilled) {
            Global.writeLog(infoPane, Color.RED, "Rules are already transformed");
            return;
        }
        if (rules != null) {
            esper = new Esper(this, (String) datasetCombo.getSelectedItem(), rules);
            esper.start();
            Global.writeLog(infoPane, Color.BLUE, "Transformation Completed");
            Global.writeLog(infoPane, Color.BLUE, "CEP Rules Deployed");
            Global.writeLog(infoPane, Color.BLUE, "CEP Engine Started");
            Global.drawLine(infoPane, Color.BLUE);
            engineKilled = false;
        } else {
            Global.writeLog(infoPane, Color.RED, "You need to select the rules first");
        }
    }//GEN-LAST:event_transformBtnActionPerformed

